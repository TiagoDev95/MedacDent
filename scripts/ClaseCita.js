//Clase cita con todos sus atributos
class Cita{
    constructor(id, nombre, apellidos, dni, numero, email, nacimiento, observaciones, cita){
        this.id = id;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.dni = dni;
        this.numero = numero;
        this.email = email;
        this.nacimiento = nacimiento;
        this.observaciones = observaciones;
        this.cita = cita;
    }
}